# Integer vector
sort(c(2L, 5L, 1L, 2L, 4L))
sort(c(2L, 5L, 1L, 2L, 4L), decreasing=TRUE)

# Double vector
sort(c(2.3, 5.12, 1.34, 2.3, 4.7))
sort(c(2.3, 5.12, 1.34, 2.3, 4.7), decreasing=TRUE)

# Character vector
sort(c('b', 'e', 'a', 'b', 'd'))
sort(c('b', 'e', 'a', 'b', 'd'), decreasing=TRUE)

# Logical vector
sort(c(TRUE, FALSE, FALSE, TRUE, FALSE))
sort(c(TRUE, FALSE, FALSE, TRUE, FALSE), decreasing=TRUE)