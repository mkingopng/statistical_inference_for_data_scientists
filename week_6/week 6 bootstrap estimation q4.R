set.seed(1)
m <- matrix(runif(2500, min=0, max=1), nrow = 100, ncol = 25)
d <- rowMeans(m)
sprintf("mean = %6.4f and stdev = %6.4f for 100 samples", mean(d), var(d))
plot(density(d), col = 'blue')
m <- matrix(runif(125000, min=0, max=1), nrow=5000, ncol = 25)
d1 <- rowMeans(m)
sprintf("mean = %6.4f and stdev = %6.4f for 5000 samples", mean(d1), var(d1))
lines(density(d1),col='red')
legend("right", legend=c("100 samples","5000 samples"), col=c("blue","red"), lty=1)